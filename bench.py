"""
Benchmark harness — this is a READ-ONLY file.
Do NOT edit this file. Only edit sort.py.

Measures sorting throughput on random integer arrays.
Prints the metric line that the experiment loop parses.
"""

import random
import time
import sys

from sort import sort_array

ARRAY_SIZE = 100_000
NUM_TRIALS = 3
SEED = 42


def generate_data(size: int, seed: int) -> list[int]:
    rng = random.Random(seed)
    return [rng.randint(-(2**31), 2**31 - 1) for _ in range(size)]


def verify_sorted(original: list[int], result: list[int]) -> bool:
    if len(result) != len(original):
        return False
    return result == sorted(original)


def run_benchmark() -> None:
    print(f"Generating {ARRAY_SIZE:,} random integers (seed={SEED})...")
    data = generate_data(ARRAY_SIZE, SEED)

    times = []
    for trial in range(1, NUM_TRIALS + 1):
        arr = list(data)  # fresh copy each trial
        start = time.perf_counter()
        result = sort_array(arr)
        elapsed = time.perf_counter() - start
        times.append(elapsed)

        if not verify_sorted(data, result):
            print(f"FAIL: output is not correctly sorted (trial {trial})")
            sys.exit(1)

        print(f"  trial {trial}: {elapsed:.4f}s")

    avg_time = sum(times) / len(times)
    throughput = int(ARRAY_SIZE / avg_time)

    print(f"\navg time: {avg_time:.4f}s")
    print(f"throughput: {throughput}")


if __name__ == "__main__":
    run_benchmark()
